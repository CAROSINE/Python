class Employee:
    name = "Mahin"
    language = "Python" # class attribute
    salary = 300000

Ahin  = Employee()
Ahin.name = "Mahin" # object attribute
print(Ahin.name, Ahin.salary, Ahin.language)


Saj = Employee()
Saj.name = "Sajol"
print(Saj.name, Saj.salary, Saj.language)

# Here name is object attributr and salary and language are
#  class attribute as they directly belong to the class



