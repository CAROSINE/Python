def goodDay(name, weather):   # we can use multiple arguments in a function
    print(f"Good day {name}! The weather is {weather}.")

goodDay("Mahin", "sunny")
goodDay("Sajol", "rainy")

# We can also use f-string for better readability
# goodDay("Mahin", "sunny") will print "Good day Mahin! The weather is sunny."